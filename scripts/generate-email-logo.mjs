// Génère lib/email-logo-base64.ts à partir de public/images/logofinal.png.
// Le logo est resizé en 400px de large, optimisé en PNG palette pour rester sous ~30 Ko.
// Embedder le logo en base64 dans l'email résout les blocages d'images externes
// par les clients mail (Gmail, Outlook, Apple Mail avec privacy proxy, etc.).
import sharp from "sharp";
import { writeFile } from "node:fs/promises";

const SOURCE = "public/images/logofinal.png";
const OUTPUT = "lib/email-logo-base64.ts";

const buf = await sharp(SOURCE)
  .resize({ width: 400 })
  .png({ quality: 90, compressionLevel: 9, palette: true })
  .toBuffer();

const b64 = buf.toString("base64");

const fileContent =
  `// Auto-generated by scripts/generate-email-logo.mjs — do not edit manually.\n` +
  `// Source: ${SOURCE} resized to 400px wide, PNG palette compressed.\n` +
  `// To regenerate after a logo change : node scripts/generate-email-logo.mjs\n` +
  `export const EMAIL_LOGO_BASE64 = "${b64}";\n`;

await writeFile(OUTPUT, fileContent, "utf8");
console.log(`✓ ${OUTPUT} written (${buf.length} bytes → ${b64.length} base64 chars)`);
